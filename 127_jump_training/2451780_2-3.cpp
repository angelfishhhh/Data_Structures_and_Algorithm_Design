/*
算法基本思想：动态规划 (Dynamic Programming) + 状态压缩 (滚动变量)
1. 问题分析：一只青蛙一次可以跳 1 级或 2 级。要到达第 n 级台阶，它的最后一步要么是从第 n-1 级跳 1 级上来的，要么是从第 n-2 级跳 2 级上来的。
2. 状态转移方程：因此，到达第 n 级的总跳法数，等于到达第 n-1 级的跳法数加上到达第 n-2 级的跳法数。即：f(n) = f(n-1) + f(n-2)。
3. 边界条件：题目规定 f(0) = 1（原地不动算 1 种），f(1) = 1。
4. 空间优化：如果我们用一个数组存储所有状态，空间复杂度是 O(n)。但因为 f(n) 只依赖于前两个状态 f(n-1) 和 f(n-2)，我们可以只用两个变量来“滚动”记录前两个状态，将空间复杂度优化到 O(1)。
5. 取模操作：题目要求对 1e9+7 (1000000007) 取模。在 C++ 中，每次执行加法运算后都必须立刻取模，否则数值过大会导致 int 型溢出。
*/

class Solution {
public:
    int numWays(int num) {
        if (num <= 1) return 1;
        
        int a = 1; // 代表 f(n-2)
        int b = 1; // 代表 f(n-1)
        int mod = 1000000007;
        
        // 从第 2 阶开始迭代计算
        for (int i = 2; i <= num; i++) {
            int current = (a + b) % mod;
            a = b;
            b = current;
        }
        
        return b;
    }
};